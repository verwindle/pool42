#include <unistd.h>
void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	while (*str != '\0' || *str)
	{
		write(1, str, 1);
		++str;
	}
}

int main() {
	char s[4] = {'f','s', 'w', '\0'};
	ft_putstr(s);
	return(0);
}
