int	ft_sqrt(int nb)
{
	int n;
	int half;

	n = 0;
	half = nb / 2;
	while (n <= half && (n * n) != nb)
	{
		n++;
	}
	if ((n * n) == nb)
	{
		return (n);
	}
	else
	{
		return (0);
	}
}
