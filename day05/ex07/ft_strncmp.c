int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	char *base;

	base = s1;
	while ((s1 - base < n) && (*s1 != '\0' || *s2 != '\0'))
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		++s1;
		++s2;
	}
	return (0);
}
